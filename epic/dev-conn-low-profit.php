		<p><strong>Frustrations and Needs:</strong> Frank has been highly aware of the presence of new sandwich shops, with the growing local population and rise of larger restaurant chains. He is finding that more young people are losing interest in his restaurant in favor of the relatively new ones. He needs a way to stay competitive, but has no budget for marketing or advertising aside from word of mouth. So, he decided he needs an online presence, professional but inexpensive.</p>
		<p><strong>Goals:</strong> Frank is looking for a web developer that won't charge a lot, or be willing to accept an alternate arrangement, to give his restaurant a professional and simple online presence with capability to place a pickup order online. He would like to have this done as soon as possible, and will connect with someone nearby with a developer's favorite medium: the Internet!</p>
